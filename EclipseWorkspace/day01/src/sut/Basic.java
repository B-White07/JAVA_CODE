package sut;

public class Basic {
	public static void main(String[] args) {
		double a = 3.1415;

		System.out.println(a);
		a = 3.1415926;
		System.out.print(a);
		byte f = 100;
		System.out.println("f=" + f);
		short b = 500;
		System.out.println("b=" + b);
		int c = 1000;
		System.out.println("c=" + c);
		long d = 1500L;
		System.out.println("d=" + d);
		boolean h = true;
		System.out.println(h);
		h = false;
		System.out.println(h);
	}
}
